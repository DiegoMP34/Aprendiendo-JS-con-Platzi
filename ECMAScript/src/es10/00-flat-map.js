const arr = [1,2,3,4,[10,11,12,13, [100,101,102]]]
console.log(arr.flat(2)); // [1,  2,  3,   4,  10, 11, 12, 13, 100, 101, 102]
console.log(arr.flat(1)); // [ 1, 2, 3, 4, 10, 11, 12, 13, [ 100, 101, 102 ] ]
console.log(arr.flat(0)); //[ 1, 2, 3, 4, [ 10, 11, 12, 13, [ 100, 101, 102 ] ] ]

